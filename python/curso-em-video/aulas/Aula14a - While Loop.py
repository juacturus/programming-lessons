# Aula 14 - Laço while - Etrutura de repetição com teste lógico.

for c in range(1, 10):
    print(c)
print('Fim')

# Refazendo utilizando while
c = 1
while c < 10:
    print(c)
    c += 1
print('Fim')
# Cada qual tem as suas características, porém o while serve para quando nós não SABEMOS o limite.
# Quando sabemos o limite, é até mais interessante usar o for, porém depende da escolha do programador.

